We <a href="index.php">American Book Company </a>is the largest wholesale distributor of closeout, remainder, hurt, promotional and bargain-priced books. “Remainder” books are overstocks that have not left the publisher’s warehouse. “Hurt” books are not necessarily damaged or defective, but refer to books returned to publishers which are not reprocessed into inventory. 

Our company was founded in 1997 by Dean Winegardner who is currently our Chairman. We source our books primarily from leading publishers such as Penguin Putnam, Harper Collins, Random House, Hachette Book Group, Perseus, and St. Martins Press as well as other publishers, book brokers and distributors. We have a variety of customers including bookstores, supermarkets, drug and dollar stores, book fairs, discount chains, colleges and more. Currently, we have our sales office in Knoxville, TN and distribution center in Jefferson City, TN of over 900,000 sq. ft. 

